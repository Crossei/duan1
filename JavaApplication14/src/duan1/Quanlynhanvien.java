    @Override
    public void sua() {
        if (check()) {
            String manv = txtmanv.getText();
            String ten = txttennv.getText();
            String ngaysinh = txtNgaysinh.getText();
                       
           
            String sdt = txtsdt.getText();
            String luong = txtluong.getText();
            String diachi = txtdiachi.getText();
            String gioitinh = txtgioitinh.getText();
             Nhanvien nv = new Nhanvien(manv, ten, ngaysinh, sdt, luong, diachi, gioitinh);
            boolean add = NhanvienDAO.update(manv, ten, ngaysinh, sdt, luong, diachi, gioitinh);
            if (add) {
                Nhanvien a = new Nhanvien(manv, ten, ngaysinh, sdt, luong, diachi, gioitinh);

                Vector row = new Vector();
                row.add(manv);
                row.add(ten);
                row.add(ngaysinh);
                row.add(sdt);
                row.add(luong);
                row.add(diachi);
                row.add(gioitinh);
                model.addRow(row);
                JOptionPane.showMessageDialog(null, "Sửa thành công!");

            } else {
                JOptionPane.showMessageDialog(null, " Thất bại", "Thông báo!", JOptionPane.ERROR_MESSAGE);

            }
        }
        
    }
    

